
test="123456"
# 取0到7-1，但是索引只有0到5,所以全部取到,  结果: 123456
print(test[0:7])  # 123456

# 取0到7-1, 步长为2, 那么取的是0, 0+2, 0+2+2, 0+2+2+2, 结果135
print(test[0:7:2])  # 135

# step = 1 > 0  start > end , 结果为空
print(test[7:0])   

# 反向取,这里一定要注意是左闭右开型，所以右开的0位不算里面， 结果是: 65432
print(test[7:0:-1]) # 65432 

# 首先123456对应索引012345, 那么从3到0，步长-2，左闭右开型，则是3, 3-2, 3-2-2 <0+1 所以结果是 索引31对应值为 42
print(test[3:0:-2])  # 42
